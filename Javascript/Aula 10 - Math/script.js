let num1 = 9.54578;
console.log(num1);

// Arredonda pra baixo
console.log(Math.floor(num1));
// Arredonda pracima
console.log(Math.ceil(num1));
// Arredonda pro mais próximo
console.log(Math.round(num1));

// Retorna o maior número da sequência
console.log(Math.max(1, 5, 2, 9, 10, 39, 100, 45, 2));
// Retorna o menor número da sequência
console.log(Math.min(1, 5, 2, 9, 10, 39, 100, 45, 2));

// Retorna um número aleatório entre 0 e 1
console.log(Math.random());

// Número aleatório entre 5 e 10
const random = Math.random() * (10 - 5) + 5;
console.log(random);

// Raiz Quadrada
console.log(10 ** 0.5);